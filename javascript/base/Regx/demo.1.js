/**
 * 将数组中相邻元素相同的合并
 * 如将： [3,2,2, 2,4,5,5,6,2,1] => [3, 2, 4, 5, 6, 2, 1]
 *        [3,2,2,4,51,51,6,2,1] => [3, 2, 4, 51, 51, 6, 2, 1]
 * @param {数组} arr 
 */
function merge(arr) {
    return arr.join(',').replace(/(\d,)\1+/g, '$1').split(',').map(e => +e)
}
console.log(merge([3,2,2, 2,4,5,5,6,2,1]));